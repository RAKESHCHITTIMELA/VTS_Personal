package vts.utils;

public class OrganizationRegistrationBean {

	private String organizationId;
	private long organizationRegisteredDate;
	private String organizationName;
	private String organizationEmail;
	private long organizationMobileNumber;
	private String organizationHouseStreetName;
	private String city;
	private String districtName;
	private String country;

	public String getOrganizationId() {
		return organizationId;
	}

	public void setOrganizationId(String organizationId) {
		this.organizationId = organizationId;
	}

	public long getOrganizationRegisteredDate() {
		return organizationRegisteredDate;
	}

	public void setOrganizationRegisteredDate(long organizationRegisteredDate) {
		this.organizationRegisteredDate = organizationRegisteredDate;
	}

	public String getOrganizationName() {
		return organizationName;
	}

	public void setOrganizationName(String organizationName) {
		this.organizationName = organizationName;
	}

	public String getOrganizationEmail() {
		return organizationEmail;
	}

	public void setOrganizationEmail(String organizationEmail) {
		this.organizationEmail = organizationEmail;
	}

	public long getOrganizationMobileNumber() {
		return organizationMobileNumber;
	}

	public void setOrganizationMobileNumber(long organizationMobileNumber) {
		this.organizationMobileNumber = organizationMobileNumber;
	}

	public String getOrganizationHouseStreetName() {
		return organizationHouseStreetName;
	}

	public void setOrganizationHouseStreetName(String organizationHouseStreetName) {
		this.organizationHouseStreetName = organizationHouseStreetName;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getDistrictName() {
		return districtName;
	}

	public void setDistrictName(String districtName) {
		this.districtName = districtName;
	}

	public String getCountry() {
		return country;
	}

	public void setCountry(String country) {
		this.country = country;
	}

}
